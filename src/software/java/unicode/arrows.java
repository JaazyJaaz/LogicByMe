    public static final String HEAVY_R_ARROW = "\u279E";    // ➞
    public static final String TRIANGLE_R_ARROW = "\u25B6"; // ▶
    public static final String BARB_R_ARROW = "\u27A4";     // ➤
    public static final String SPLIT_R_ARROW = "\u21A6";     // ↦
    public static final String ARROW_RIGHT_HOOK = "\u21A3";  // ↣
    public static final String ARROW_WITH_TAIL = "\u21A0";   // ↠

    public static final String LEFT_RIGHT_ARROW = "\u2194"; // ↔
    public static final String DOUBLE_LEFT_RIGHT_ARROW = "\u21D4"; // ⇔
    public static final String LONG_LEFT_RIGHT_ARROW = "\u27F7";   // ⟷
    public static final String REVERSE_ARROWS = "\u21CB";   // ⇋

    public static final String L_ARROW = "\u2190";          // ←
    public static final String FAR_L_ARROW = "\u21FD";      // ⇽
    public static final String DOUBLE_L_ARROW = "\u21D0";   // ⇐
    public static final String HOOK_L_ARROW = "\u21A9";     // ↩
    public static final String LONG_L_ARROW = "\u27F5";     // ⟵
    public static final String DASHED_L_ARROW = "\u21E0";   // ⇠
    public static final String CURVED_L_ARROW = "\u21A9";   // ↩
    public static final String HEAVY_L_ARROW = "\u279A";    // ➚
    public static final String TRIANGLE_L_ARROW = "\u25C0"; // ◀
    public static final String BARB_L_ARROW = "\u27A1";     // 
    public static final String SPLIT_L_ARROW = "\u21A4";    // ↤
    public static final String ARROW_LEFT_HOOK = "\u21A2";  // 
    public static final String ARROW_WITH_DOUBLE_TAIL = "\u21A2"; // ↢


    // up/down arrows
    public static final String UP_ARROW = "\u2191";         // ↑
    public static final String DOUBLE_UP_ARROW = "\u21D1";  // ⇑
    public static final String LONG_UP_ARROW = "\u290A";    // ⤊
    public static final String HEAVY_UP_ARROW = "\u279A";   // ➚
    public static final String TRIANGLE_UP_ARROW = "\u25B2";// ▲
    public static final String BARB_UP_ARROW = "\u2B06";    // ⬆
    public static final String CURVED_UP_RIGHT_ARROW = "\u2934";// ⤴

    public static final String UP_DOWN_ARROW = "\u2195";    // ↕
    public static final String DOUBLE_UP_DOWN_ARROW = "\u21D5"; // ⇕

    public static final String DOWN_ARROW = "\u2193";       // ↓
    public static final String DOUBLE_DOWN_ARROW = "\u21D3";// ⇓
    public static final String LONG_DOWN_ARROW = "\u290B";  // ⤋
    public static final String HEAVY_DOWN_ARROW = "\u279B"; // ➛
    public static final String TRIANGLE_DOWN_ARROW = "\u25BC";    // ▼
