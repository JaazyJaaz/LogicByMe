package unicode;

/** ANSI escape codes
 * Quick reference to Greek letters
 */
public class GreekLetters {
    public static final String OHM = "\u03A8";     // Ω
    public static final String PI = "\u03C0";      // π
    public static final String ALPHA = "\u03B1";   // α
    public static final String ZETA = "\u03B6";    // ζ
    public static final String THETA = "\u03F4";   // ϴ
    public static final String PHI = "\u03D5";     // ϕ
    public static final String MU = "\u03BC";      // μ
    public static final String LAMBDA = "\u03BB";  // λ
    public static final String SIGMA = "\u03A3";   // Σ
    public static final String OMEGA = "\u03C9";   // ω
}