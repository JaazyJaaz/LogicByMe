package unicode;

/** ANSI escape codes
 * Quick reference for mathematical symbols and constants.
 */
public class MathSymbols {

    // example
    public static final String COS_PHI = "Cos" + GreekLetters.PHI;
    public static final String SIN_PHI = "Sin" + GreekLetters.PHI;

    // math it up
    public static final String PLUS_MINUS = "\u00B1";    // ±
    public static final String MULTIPLY = "\u00D7";      // ×
    public static final String DIVIDE = "\u00F7";        // ÷
    public static final String INFINITY = "\u221E";      // ∞
    public static final String DEGREE = "\u00B0";        // °
    public static final String SQUARE_ROOT = "\u221A";   // √
    public static final String CUBE_ROOT = "\u221B";     // ∛
    public static final String FOURTH_ROOT = "\u221C";   // ∜
    public static final String SUM = "\u2211";           // ∑
    public static final String PRODUCT = "\u220F";       // ∏
    public static final String INTEGRAL = "\u222B";      // ∫
    public static final String DOUBLE_INTEGRAL = "\u222C"; // ∬
    public static final String FOR_ALL = "\u2200";       // ∀
    public static final String THERE_EXISTS = "\u2203";  // ∃
    public static final String ELEMENT_OF = "\u2208";    // ∈
    public static final String NOT_ELEMENT_OF = "\u2209";// ∉
    public static final String APPROX = "\u2248";        // ≈
    public static final String NOT_EQUAL = "\u2260";     // ≠
    public static final String LESS_EQUAL = "\u2264";    // ≤
    public static final String GREATER_EQUAL = "\u2265"; // ≥

}
